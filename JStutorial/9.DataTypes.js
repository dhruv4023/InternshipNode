// Number: Represents numeric values (integer or floating-point)
var x = 5; // integer
var y = 3.14; // floating-point number

// String: Represents a sequence of characters (text)
var name = "John";

// Boolean: Represents either true or false
var isTrue = true;

// Null: Represents the intentional absence of any object value
var myVar = null;

// Undefined: Represents an uninitialized or undefined value
var undefinedVar;

// Symbol: Unique and immutable data type, often used as identifiers for object properties
var mySymbol = Symbol("unique");

// Object: Represents a collection of key-value pairs
var person = {
  name: "Alice",
  age: 30,
  isStudent: false,
};

// Array: Represents an ordered list of values
var numbers = [1, 2, 3, 4, 5];

// Function: Represents a reusable block of code
function add(x, y) {
  return x + y;
}

// Date: Represents a specific point in time
var currentDate = new Date();

// RegExp: Represents a regular expression for pattern matching
var pattern = /[a-z]+/;

