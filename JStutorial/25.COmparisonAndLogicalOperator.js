x = 5;

// ==	equal to
console.log(x == 8); //false
console.log(x == 5); //true
console.log(x == "5"); //	true

// ===	equal value and equal type
console.log(x === 5); //  true
console.log(x === "5"); //  	false

// !=	not equal
console.log(x != 8); //	true

// !==	not equal value or not equal type
console.log(x !== 5); //false
console.log(x !== "5"); //	true
console.log(x !== 8); //true

// >	greater than
console.log(x > 8); //false

// <	less than
console.log(x < 8); //true

// >=	greater than or equal to
console.log(x >= 8); //	false

// <=	less than or equal to
console.log(x <= 8); //	true
