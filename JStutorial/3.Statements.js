var x; // Variable declaration statement

x = 5; // Assignment statement

console.log("Hello, World!"); // Function invocation statement

// loop statement
for (var i = 0; i < 5; i++) {
  console.log(i);
}

// conditional statement
if (x > 0) {
   console.log("x is positive");
} else {
   console.log("x is non-positive");
}

